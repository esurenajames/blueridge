<script>
document.querySelectorAll('.tab-link').forEach(tab => {
    tab.addEventListener('click', function (event) {
        event.preventDefault();
        
        // Hide all tab contents
        document.querySelectorAll('.tab-content > div').forEach(content => {
            content.classList.add('hidden');
        });
        
        // Remove active class from all tabs
        document.querySelectorAll('.tab-link').forEach(link => {
            link.classList.remove('text-blue-800', 'bg-gray-200');
        });
        
        // Show the selected tab content
        const tabContent = document.querySelector(`[data-tab-content="${this.dataset.tab}"]`);
        if (tabContent) {
            tabContent.classList.remove('hidden');
        }
        
        // Set the active class on the clicked tab
        this.classList.add('text-blue-800', 'bg-gray-200');
    });
});
</script>
